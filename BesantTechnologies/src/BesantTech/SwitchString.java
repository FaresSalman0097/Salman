package BesantTech;

public class SwitchString {
	public static void main(String args[]) {
		String birthday = "SEPTEMBER";
		switch(birthday) {
		case "JUNE": 
			System.out.println("parents birthday!!");
			break;
		case "DECEMBER":
			System.out.println("Suhi birthday!!");
			break;
		case "SEPTEMBER":
			System.out.println("Sami birthday!!");
			break;
		case "AUGUST":
			System.out.println("My birthday!!");
			break;
		default:
			System.out.println("Yaarukum birthday ila!!");
		
		}
	}

}
