package BesantTech;

public class SwitchStatement {
public static void main(String[] args) {
	int i=2;
	switch(i) {
	case 1:
		System.out.println("Ibrahim");
		break;
	case 2:
		System.out.println("Nasrin");
		break;
	case 3:
		System.out.println("Sohail");
		break;
	case 4:
		System.out.println("Salmeera");
		break;
	default:
		System.out.println("Ibrahim");
	}
	
}
}
