package practice;

public class A04AreaAndPerimeterOfTriangle {
	public static void main(String[] args) {
		int base = 10;
		int height = 5;
		int side1 = 7;
		double side2 = 5.5;
		double area = (base * height) / 2;
		System.out.println(area);
		double perimeter = side1 + side2 + base;
		System.out.println(perimeter);
	}
}
